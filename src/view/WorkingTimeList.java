    public String ConvertFormatTime(String time) {
        String res = "";
        String hour = "";
        String kind = "";

        for (int i = 0; i < 2; i++) {
            hour += time.charAt(i);
        }
        for (int i = 6; i <= 7; i++) {
            kind += time.charAt(i);
        }
        if (kind.equals("PM")) {
            hour = (Integer.parseInt(hour) + 12) + "";
        }
        res += hour;
        for (int i = 2; i < 5; i++) {
            res += time.charAt(i);
        }
        res += ":00";
        return res;
    }

